package datatype;

public class Calculator {

    public static long add(int firstNumber, int secondNumber){
        return firstNumber + secondNumber;
    }
    public static long add(long firstNumber, long secondNumber){
        return firstNumber + secondNumber;
    }
    public static float add(float firstNumber, float secondNumber){
        return firstNumber + secondNumber;
    }
    public static double add(double firstNumber, double secondNumber){
        return firstNumber + secondNumber;
    }
    public static long subtract(int firstNumber, int secondNumber){
        return firstNumber - secondNumber;
    }

    public static long subtract(long firstNumber, long secondNumber){
        return firstNumber - secondNumber;
    }
    public static float subtract(float firstNumber, float secondNumber){
        return firstNumber - secondNumber;
    }
    public static double subtract(double firstNumber, double secondNumber){
        return firstNumber - secondNumber;
    }

    public static long multiply(int firstNumber, int secondNumber){
        return firstNumber * secondNumber;
    }

    public static long multiply(long firstNumber, long secondNumber){
        return firstNumber * secondNumber;
    }
    public static float multiply(float firstNumber, float secondNumber){
        return firstNumber * secondNumber;
    }
    public static double multiply(double firstNumber, double secondNumber){
        return firstNumber * secondNumber;
    }

    public static long division(int firstNumber, int secondNumber){
        return firstNumber / secondNumber;
    }

    public static long division(long firstNumber, long secondNumber){
        return firstNumber / secondNumber;
    }
    public static float division(float firstNumber, float secondNumber){
        return firstNumber / secondNumber;
    }
    public static double division(double firstNumber, double secondNumber){
        return firstNumber / secondNumber;
    }
}
